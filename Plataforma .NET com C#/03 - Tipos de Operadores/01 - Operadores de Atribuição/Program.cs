// = é o operador de atribuição
int a = 10;
int b = 9;
int c = a + b; // O valor de C será 19 (a + b)
Console.WriteLine(c);

// Combinando operadores
c = c + 5; // c está recebendo o valor c (19) + 5
Console.WriteLine(c); // Resultado = 24

// O código abaixo trás o mesmo resultado do código acima !
c += 6; // += significa que a variável c irá receber o seu valor (24) e irá receber uma soma (6)
Console.WriteLine(c); // Resultado = 30

c -= 15; // O valor atribuido a variável c, é =  c(30) - 15;
Console.WriteLine(c); // Resultado = 15